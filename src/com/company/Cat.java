package com.company;

public class Cat {
    String breed, color;
    float weight;

    public String getBreed(){
        return this.breed;
    }

    public void setBreed(String breed){
        this.breed = breed;
    }

    public String getColor(){
        return this.color;
    }

    public void setColor(String color){
        this.color = color;
    }

    public float getWeight(){
        return this.weight;
    }

    public void setWeight(float weight){
        this.weight = weight;
    }
}
